public class Card {
    
    
    private int time;
    private int price;
    
    public Card(){}
    
    // set method for time
    public void setTime(int time){       
        this.time = time;
    }
    // get method for time
    public int getTime() {
        return time;
    }
    
    // set method for price
    public void setPrice(int price){    
        this.price = price;
    }
    // get method for price
    public int getPrice() {
        return price;
    }
}
